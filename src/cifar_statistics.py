def colorfulness(flat_img):
    """Hasler & Süsstrunk (2003) colorfulness metric."""
    img = to_hwc(flat_img)
    R, G, B = img[:,:,0], img[:,:,1], img[:,:,2]
    rg = R.astype(float) - G.astype(float)
    yb = 0.5*(R.astype(float)+G.astype(float)) - B.astype(float)
    mu_rg, sig_rg = rg.mean(), rg.std()
    mu_yb, sig_yb = yb.mean(), yb.std()
    sig_rgyb = np.sqrt(sig_rg**2 + sig_yb**2)
    mu_rgyb  = np.sqrt(mu_rg**2  + mu_yb**2)
    return sig_rgyb + 0.3*mu_rgyb

