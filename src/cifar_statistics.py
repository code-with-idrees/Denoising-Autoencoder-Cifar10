def pearson_ci(r, n, alpha=0.05):
    z   = np.arctanh(r)
    se  = 1.0 / np.sqrt(n - 3)
    z_crit = sp.norm.ppf(1 - alpha/2)
    lo  = np.tanh(z - z_crit*se)
    hi  = np.tanh(z + z_crit*se)
    return lo, hi

