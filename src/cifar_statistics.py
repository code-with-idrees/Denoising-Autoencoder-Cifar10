def full_stats(arr, name=""):
    """Return dict of descriptive stats for a 1-D or N-D float array."""
    flat = arr.flatten()
    q1, q3 = np.percentile(flat, [25, 75])
    return {
        "name":   name,
        "n":      flat.size,
        "min":    flat.min(),
        "max":    flat.max(),
        "range":  flat.max() - flat.min(),
        "mean":   flat.mean(),
        "median": np.median(flat),
        "std":    flat.std(),
        "var":    flat.var(),
        "cv":     flat.std() / flat.mean() * 100,   # coefficient of variation %
        "q1":     q1,
        "q3":     q3,
        "iqr":    q3 - q1,
        "skew":   float(sp_stats.skew(flat)),
        "kurt":   float(sp_stats.kurtosis(flat)),   # excess kurtosis
        "p05":    np.percentile(flat, 5),
        "p95":    np.percentile(flat, 95),
        "p99":    np.percentile(flat, 99),
    }

