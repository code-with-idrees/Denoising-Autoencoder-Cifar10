def image_entropy(gray_uint8):
    """Shannon entropy of pixel histogram."""
    counts = np.bincount(gray_uint8.flatten().astype(np.uint8), minlength=256)
    probs  = counts / counts.sum()
    probs  = probs[probs > 0]
    return -np.sum(probs * np.log2(probs))

