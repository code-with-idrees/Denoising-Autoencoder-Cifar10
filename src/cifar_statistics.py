# Compute autocorrelation of a single channel's rows (averaged over images)
def mean_autocorr(X_imgs_c, max_lag=16):
    """
    X_imgs_c: (N,32,32) grayscale images
    Returns average autocorrelation across rows and images for lag 0..max_lag
    """
    acf = np.zeros(max_lag+1)
    N, H, W = X_imgs_c.shape
    for lag in range(max_lag+1):
        pairs = []
        for row in range(H):
            if lag == 0:
                v = X_imgs_c[:, row, :]
                pairs.append((v*v).mean())
            else:
                v1 = X_imgs_c[:, row, :W-lag].reshape(-1)
                v2 = X_imgs_c[:, row, lag:  ].reshape(-1)
                pairs.append(np.corrcoef(v1, v2)[0,1])
        acf[lag] = np.mean(pairs)
    acf /= acf[0]
    return acf

